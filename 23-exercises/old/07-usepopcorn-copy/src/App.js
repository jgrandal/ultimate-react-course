// Using Component Composition to make a Reusable Component, Build better layouts and solving prop drilling
// ListBox and WatchedBox are almost the same so we can join then in a single reusable component
function Box({ children }) {
  const [isOpen, setIsOpen] = useState(true);
  return (
    <div className="box">
      <button className="btn-toggle" onClick={() => setIsOpen((open) => !open)}>
        {isOpen ? '‚Äì' : '+'}
      </button>
      {isOpen && children}
    </div>
  );
}

